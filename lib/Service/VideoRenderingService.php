    private function loadEvent(int $eventId, string $userId): array {
        $qb = $this->db->getQueryBuilder();
        $qb->select('*')
            ->from('reel_events')
            ->where($qb->expr()->eq('id',       $qb->createNamedParameter($eventId, IQueryBuilder::PARAM_INT)))
