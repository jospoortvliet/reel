    private function resolveFilePaths(array $media, Folder $userFolder, string $userId, ?callable $onDebug = null): array {
        $items = [];
        $fileIds = array_map(static fn(array $row): int => (int)$row['file_id'], $media);
        $faceMap = $this->memoriesRepository->loadFaceDetections($fileIds, $userId);
        $subtleRunMotion = null;
        $subtleRunRemaining = 0;
        $lastSubtleRunMotion = null;
        $outputOrientation = $this->config->getUserValue(
            $userId,
            Application::APP_ID,
            'output_orientation',
            'landscape_16_9',
        );
        $targetAspect = match ($outputOrientation) {
            'portrait_9_16' => 9.0 / 16.0,
            'square_1_1' => 1.0,
            default => $this->targetAspect(),
        };

        foreach ($media as $row) {
            try {
                $fileId = (int)$row['file_id'];
                $useLive = !empty($row['use_live_video']) && !empty($row['liveid']);

                // If live video requested, try to find the .mov sibling via name-swap
                if ($useLive) {
                    $movFileId = $this->memoriesRepository->findLiveVideoFileId($fileId);
                    if ($movFileId !== null) {
                        $fileId = $movFileId;
                    } else {
                        $this->logger->info('Reel: .mov not found for live photo {id}, using still', ['id' => $row['file_id']]);
                        $useLive = false;
                    }
                }

                $files = $userFolder->getById($fileId);
                if (empty($files)) {
                    $this->logger->warning('Reel: could not find file {id}', ['id' => $fileId]);
                    continue;
                }

                $file      = $files[0];
                $localPath = $this->getLocalPath($file);

                if (!file_exists($localPath)) {
                    $this->logger->warning('Reel: file does not exist at {path}', ['path' => $localPath]);
                    continue;
                }

                $isVideo  = $useLive || (bool)$row['isvideo'];
                $interestingFocus = $isVideo
                    ? null
                    : $this->detectInterestingFocus($localPath);

                $dims = $isVideo
                    ? ['w' => (int)($row['w'] ?? 0), 'h' => (int)($row['h'] ?? 0)]
                    : $this->resolveImageDimensions(
                        $localPath,
                        (int)($row['w'] ?? 0),
                        (int)($row['h'] ?? 0),
                    );

                if ($isVideo) {
                    $motion = [];
                    // Video/live clips are "breakers" that reset subtle run grouping.
                    $subtleRunMotion = null;
                    $subtleRunRemaining = 0;
                } else {
                    if ($subtleRunMotion === null || $subtleRunRemaining <= 0) {
                        [$subtleRunMotion, $subtleRunRemaining] = $this->pickNextSubtleRun($lastSubtleRunMotion, (int)$row['file_id']);
                        $lastSubtleRunMotion = $subtleRunMotion;
                    }

                    $motion = $this->buildPhotoMotionSubtle(
                        (int)$row['file_id'],
                        $faceMap[(int)$row['file_id']] ?? [],
                        (int)$dims['w'],
                        (int)$dims['h'],
                        $interestingFocus,
                        $subtleRunMotion,
                    );
                    $motion['style'] = 'apple_subtle';
                    $motion['run_motion'] = $subtleRunMotion;
                    $motion['run_remaining'] = $subtleRunRemaining;
                    $dimW = (int)$dims['w'];
                    $dimH = (int)$dims['h'];
                    $motion['image_aspect'] = ($dimH > 0) ? ($dimW / $dimH) : $this->targetAspect();
                    $subtleRunRemaining--;
                }

                $duration = $isVideo
                    ? $this->resolveVideoDuration($localPath, $row, $useLive)
                    : (float)$motion['duration'];

                $videoStartAt = 0.0;
                if ($isVideo) {
                    [$videoStartAt, $duration] = $this->resolveVideoWindow($row, $duration, $useLive);
                }

                if ($onDebug) {
                    $sourceW = (int)$dims['w'];
                    $sourceH = (int)$dims['h'];
                    $sourceAspect = $sourceH > 0 ? ($sourceW / $sourceH) : 0.0;
                    $msg = sprintf(
                        '  Motion debug: file_id=%d src=%dx%d aspect=%.4f',
                        (int)$row['file_id'],
                        $sourceW,
                        $sourceH,
                        $sourceAspect,
                    );

                    if ($isVideo) {
                        $msg .= sprintf(' duration=%.2fs start=%.2fs live=%s', $duration, $videoStartAt, $useLive ? 'yes' : 'no');
                    } else {
                        $msg .= sprintf(' fy=%.4f duration=%.2fs', (float)($motion['fy'] ?? 0.5), (float)$duration); // this needs fixing - to contain info about the actual motion (zoom/pan, if any).
                    }

                    $msg .= ' path=' . basename($localPath);
                    $onDebug($msg);
                }

                $items[] = [
                    'file_id'  => (int)$row['file_id'],
                    'path'     => $localPath,
                    'is_video' => $isVideo,
                    'duration' => $duration,
                    'start_at' => $videoStartAt,
                    'w' => (int)$dims['w'],
                    'h' => (int)$dims['h'],
                    'liveid'   => $row['liveid'],
                    'motion'   => $motion,
                ];
            } catch (\Throwable $e) {
                $this->logger->warning('Reel: error resolving file {id}: {msg}', [
                    'id'  => $row['file_id'],
                    'msg' => $e->getMessage(),
                ]);
            }
        }

        return $items;
    }

    private function pickNextSubtleRun(?string $lastMotion, int $fileId): array {
        $motions = ['zoom_in', 'zoom_out'];
        $idx = abs((int)crc32($fileId . ':motion')) % count($motions);
        $motion = $motions[$idx];
        if ($lastMotion !== null && $motion === $lastMotion) {
            $motion = $motions[($idx + 1) % count($motions)];
        }

        $runLength = 2 + (abs((int)crc32($fileId . ':run')) % 3); // 2..4

        return [$motion, $runLength];
    }

    private function resolveVideoDuration(string $localPath, array $row, bool $isLive): float {
        $metadataDuration = (float)($row['video_duration'] ?? 0.0);
        $probedDuration = $this->probeVideoDurationSeconds($localPath);

        // For live photos, prefer real MOV metadata duration.
        // If probing fails, fall back to DB metadata and then a conservative default.
        if ($isLive) {
            if ($probedDuration !== null && $probedDuration > 0.0) {
                return $this->clamp($probedDuration, 0.6, self::MAX_CLIP_DURATION);
            }

            if ($metadataDuration > 0.0) {
                return $this->clamp($metadataDuration, 0.6, self::MAX_CLIP_DURATION);
            }

            return 2.8;
        }

        // Regular videos keep existing behavior but use probing as a fallback.
        $baseDuration = $metadataDuration > 0.0
            ? $metadataDuration
            : ($probedDuration ?? 0.0);

        if ($baseDuration <= 0.0) {
            return self::MAX_CLIP_DURATION;
        }

        return min($baseDuration, self::MAX_CLIP_DURATION);
    }

    private function resolveVideoWindow(array $row, float $baseDuration, bool $isLive): array {
        $baseDuration = $this->clamp($baseDuration, 0.6, self::MAX_CLIP_DURATION);
        if ($isLive) {
            return [0.0, $baseDuration];
        }

        $settings = [];
        if (!empty($row['edit_settings'])) {
            $decoded = json_decode((string)$row['edit_settings'], true);
            if (is_array($decoded)) {
                $settings = $decoded;
            }
        }

        $sourceDuration = (float)($row['video_duration'] ?? 0.0);
        if ($sourceDuration <= 0.0) {
            $sourceDuration = $baseDuration;
        }

        $requestedStart = isset($settings['video_start']) && is_numeric($settings['video_start'])
            ? max(0.0, (float)$settings['video_start'])
            : null;
        $requestedLengthRaw = $settings['video_length'] ?? ($settings['video_duration'] ?? null);
        $requestedLength = is_numeric($requestedLengthRaw)
            ? max(0.6, (float)$requestedLengthRaw)
            : null;

        $defaultLength = min(self::MAX_CLIP_DURATION, $sourceDuration);
        $length = $requestedLength !== null ? min($requestedLength, self::MAX_CLIP_DURATION) : $defaultLength;
        $length = min($length, max(0.6, $sourceDuration));

        if ($requestedStart !== null) {
            $start = $requestedStart;
        } elseif ($sourceDuration > ($length + 0.05)) {
            // For long videos, default to a centered segment instead of always the start.
            $start = max(0.0, ($sourceDuration - $length) / 2.0);
        } else {
            $start = 0.0;
        }

        $maxStart = max(0.0, $sourceDuration - $length);
        $start = min($start, $maxStart);

        return [$start, $length];
    }

    private function probeVideoDurationSeconds(string $localPath): ?float {
        $cmd = [
            'ffprobe',
            '-v', 'error',
            '-show_entries', 'format=duration',
            '-of', 'default=noprint_wrappers=1:nokey=1',
            $localPath,
        ];

        $process = proc_open(
            $cmd,
            [
                0 => ['pipe', 'r'],
                1 => ['pipe', 'w'],
                2 => ['pipe', 'w'],
            ],
            $pipes
        );

        if (!is_resource($process)) {
            return null;
        }

        fclose($pipes[0]);
        $stdout = stream_get_contents($pipes[1]);
        $stderr = stream_get_contents($pipes[2]);
        fclose($pipes[1]);
        fclose($pipes[2]);
        $exitCode = proc_close($process);

        if ($exitCode !== 0 || $stdout === false) {
            if (!empty($stderr)) {
                $this->logger->debug('Reel: ffprobe duration failed: {err}', ['err' => trim($stderr)]);
            }
            return null;
        }

        $duration = (float)trim($stdout);
        return $duration > 0.0 ? $duration : null;
    }

    private function buildPhotoMotionSubtle(
        int $fileId,
        array $faces,
        int $imageW,
        int $imageH,
        ?array $interestingFocus,
        string $runMotion,
    ): array {
        $seed = crc32((string)$fileId);
        $jx = (($seed & 0xffff) / 0xffff) - 0.5;
        $jy = ((($seed >> 16) & 0xffff) / 0xffff) - 0.5;
        $maxNoBlurZoom = $this->maxNoBlurZoom($imageW, $imageH);

        $baseFx = 0.5 + ($jx * 0.04);
        $baseFy = 0.5 + ($jy * 0.03);

        if (count($faces) > 0) {
            $sumX = 0.0;
            $sumY = 0.0;
            foreach ($faces as $face) {
                $mapped = $this->mapFocusToCoverFrame((float)$face['fx'], (float)$face['fy'], $imageW, $imageH);
                $sumX += (float)$mapped['fx'];
                $sumY += (float)$mapped['fy'];
            }

            $baseFx = $sumX / count($faces);
            $baseFy = ($sumY / count($faces)) - 0.02;
        } elseif ($interestingFocus !== null) {
            $mapped = $this->mapFocusToCoverFrame((float)$interestingFocus['fx'], (float)$interestingFocus['fy'], $imageW, $imageH);
            $baseFx = (0.65 * (float)$mapped['fx']) + (0.35 * $baseFx);
            $baseFy = (0.65 * (float)$mapped['fy']) + (0.35 * $baseFy);
        }

        $fx = $this->clamp($baseFx, 0.20, 0.80);
        $fy = $this->clamp($baseFy, 0.18, 0.82);
        $subtleZoom = min($maxNoBlurZoom, 1.06);
        $subtlePanZoom = min($maxNoBlurZoom, 1.02);
        $panDelta = 0.06;

        if (!empty($faces)) {
            $primaryFace = $this->pickPrimaryFace($faces);
            $mappedFace = $this->mapFaceBoxToCoverFrame(
                (float)$primaryFace['w'],
                (float)$primaryFace['h'],
                $imageW,
                $imageH,
            );
            $faceSize = max((float)$mappedFace['w'], (float)$mappedFace['h'], 0.08);
            $faceTargetZoom = $this->clamp(0.42 / $faceSize, 1.02, 1.10);
            $subtleZoom = min($maxNoBlurZoom, max($subtleZoom, $faceTargetZoom));
        }

        if ($runMotion === 'zoom_out') {
            return [
                'fx' => $fx,
                'fy' => $fy,
                'zoom_start' => $subtleZoom,
                'zoom_end' => 1.0,
                'duration' => self::PHOTO_DURATION,
            ];
        }

        if ($runMotion === 'pan_left') {
            $fxStart = $this->clamp($fx + $panDelta, 0.24, 0.84);
            $fxEnd = $this->clamp($fx - $panDelta, 0.16, 0.76);

            return [
                'mode' => 'pan_x',
                'pan_engine' => 'crop_ease_subtle',
                'fx_start' => $fxStart,
                'fx_end' => $fxEnd,
                'fy' => $fy,
                'zoom_start' => 1.0,
                'zoom_end' => $subtlePanZoom,
                'duration' => self::PHOTO_DURATION,
            ];
        }

        if ($runMotion === 'pan_right') {
            $fxStart = $this->clamp($fx - $panDelta, 0.16, 0.76);
            $fxEnd = $this->clamp($fx + $panDelta, 0.24, 0.84);

            return [
                'mode' => 'pan_x',
                'pan_engine' => 'crop_ease_subtle',
                'fx_start' => $fxStart,
                'fx_end' => $fxEnd,
                'fy' => $fy,
                'zoom_start' => 1.0,
                'zoom_end' => $subtlePanZoom,
                'duration' => self::PHOTO_DURATION,
            ];
        }

        return [
            'fx' => $fx,
            'fy' => $fy,
            'zoom_start' => 1.0,
            'zoom_end' => $subtleZoom,
            'duration' => self::PHOTO_DURATION,
        ];
    }

    private function buildPhotoMotion(
        int $fileId,
        array $faces,
        int $imageW,
        int $imageH,
        ?array $interestingFocus,
        float $mismatchThreshold,
        float $sweepMargin,
        float $panoramaSweepMargin,
        float $faceZoomTargetFill,
        float $nonFaceZoomEnd,
    ): array {
        $seed = crc32((string)$fileId);
        $jx = (($seed & 0xffff) / 0xffff) - 0.5;
        $jy = ((($seed >> 16) & 0xffff) / 0xffff) - 0.5;
        $safeMin = 0.08;
        $safeMax = 0.92;
        $maxNoBlurZoom = $this->maxNoBlurZoom($imageW, $imageH);
        $targetAspect = $this->targetAspect();
        $aspect = ($imageW > 0 && $imageH > 0) ? ($imageW / $imageH) : $targetAspect;
        $ratioToTarget = max($aspect / $targetAspect, $targetAspect / $aspect);
        $isMismatch = $ratioToTarget >= $mismatchThreshold;
        $isPanoramaWide = $aspect > ($targetAspect * 1.9);
        $isPanoramaTall = $aspect < ($targetAspect / 1.9);

        if (count($faces) === 1) {
            $face = $faces[0];
            $mapped = $this->mapFocusToCoverFrame((float)$face['fx'], (float)$face['fy'], $imageW, $imageH);
            $fx = $this->clamp($mapped['fx'], 0.16, 0.84);
            $fy = $this->clamp($mapped['fy'] - 0.03, 0.14, 0.86);
            $mappedFace = $this->mapFaceBoxToCoverFrame((float)$face['w'], (float)$face['h'], $imageW, $imageH);
            $faceSize = max((float)$mappedFace['w'], (float)$mappedFace['h'], 0.08);

            // Start at full-frame and end at target where the face fills configured share of viewport.
            $targetZoom = max(1.0, $faceZoomTargetFill / $faceSize);
            $zoomEnd = $this->clamp(min($targetZoom, $maxNoBlurZoom), 1.0, $maxNoBlurZoom);

            return [
                'fx' => $this->clamp($fx, $safeMin, $safeMax),
                'fy' => $this->clamp($fy, $safeMin, $safeMax),
                'zoom_start' => 1.0,
                'zoom_end' => $zoomEnd,
                'duration' => self::PHOTO_DURATION,
            ];
        }

        if (count($faces) > 1) {
            // Group framing: center on the face group and zoom to configured occupancy (capped).
            $minX = 1.0;
            $minY = 1.0;
            $maxX = 0.0;
            $maxY = 0.0;
            foreach ($faces as $face) {
                $mapped = $this->mapFocusToCoverFrame((float)$face['fx'], (float)$face['fy'], $imageW, $imageH);
                $fx = (float)$mapped['fx'];
                $fy = (float)$mapped['fy'];
                $mappedFace = $this->mapFaceBoxToCoverFrame((float)$face['w'], (float)$face['h'], $imageW, $imageH);
                $fw = (float)$mappedFace['w'];
                $fh = (float)$mappedFace['h'];
                $minX = min($minX, $fx - ($fw / 2.0));
                $maxX = max($maxX, $fx + ($fw / 2.0));
                $minY = min($minY, $fy - ($fh / 2.0));
                $maxY = max($maxY, $fy + ($fh / 2.0));
            }
            $groupW = max(0.08, $maxX - $minX);
            $groupH = max(0.08, $maxY - $minY);
            $groupSize = max($groupW, $groupH);
            $targetZoom = max(1.0, $faceZoomTargetFill / $groupSize);
            $zoomEnd = $this->clamp(min($targetZoom, $maxNoBlurZoom), 1.0, $maxNoBlurZoom);
            $groupFx = $this->clamp(($minX + $maxX) / 2.0, $safeMin, $safeMax);
            $groupFy = $this->clamp((($minY + $maxY) / 2.0) - 0.02, $safeMin, $safeMax);

            $extra = 0.0;
            for ($k = 2; $k <= count($faces); $k++) {
                $extra += (0.55 / ($k - 1));
            }

            return [
                'fx' => $groupFx,
                'fy' => $groupFy,
                'zoom_start' => 1.0,
                'zoom_end' => $zoomEnd,
                'duration' => min(5.0, self::PHOTO_DURATION + min(1.8, $extra)),
            ];
        }

        if ($isMismatch) {
            $mappedFocus = $interestingFocus !== null
                ? $this->mapFocusToCoverFrame((float)$interestingFocus['fx'], (float)$interestingFocus['fy'], $imageW, $imageH)
                : ['fx' => 0.5 + ($jx * 0.04), 'fy' => 0.5 + ($jy * 0.04)];

            $horizontalSweep = $aspect > $targetAspect;
            $edgeStart = ($isPanoramaWide || $isPanoramaTall)
                ? $panoramaSweepMargin
                : $sweepMargin;
            $edgeEnd = 1.0 - $edgeStart;
            $dirForward = ((($seed >> 3) & 1) === 0);
            $start = $dirForward ? $edgeStart : $edgeEnd;
            $end = $dirForward ? $edgeEnd : $edgeStart;
            $configuredSweepZoom = $this->clamp($nonFaceZoomEnd * 0.70, 1.03, 1.26);
            $configuredPanoramaSweepZoom = $this->clamp($nonFaceZoomEnd * 0.58, 1.01, 1.12);
            $sweepZoom = ($isPanoramaWide || $isPanoramaTall)
                ? min($maxNoBlurZoom, $configuredPanoramaSweepZoom)
                : min($maxNoBlurZoom, $configuredSweepZoom);

            if ($horizontalSweep) {
                return [
                    'mode' => 'pan_x',
                    'pan_engine' => 'crop_ease',
                    'fx_start' => $start,
                    'fx_end' => $end,
                    'fy' => $this->clamp((float)$mappedFocus['fy'], 0.14, 0.86),
                    'zoom_start' => 1.0,
                    'zoom_end' => $sweepZoom,
                    'duration' => self::PHOTO_DURATION,
                ];
            }

            return [
                'mode' => 'pan_y',
                'pan_engine' => 'crop_ease',
                'fx' => $this->clamp((float)$mappedFocus['fx'], 0.14, 0.86),
                'fy_start' => $start,
                'fy_end' => $end,
                'zoom_start' => 1.0,
                'zoom_end' => $sweepZoom,
                'duration' => self::PHOTO_DURATION,
            ];
        }

        // No faces: zoom on an "interesting" detected point with deterministic per-image jitter.
        // If saliency is weak or biased, this still avoids always picking the same viewport region.
        if ($interestingFocus !== null) {
            $mappedFocus = $this->mapFocusToCoverFrame((float)$interestingFocus['fx'], (float)$interestingFocus['fy'], $imageW, $imageH);
            // Blend saliency with seeded per-image variation so no-face shots don't converge.
            $baseFx = ($mappedFocus['fx'] * 0.72) + ((0.5 + ($jx * 0.40)) * 0.28);
            $baseFy = ($mappedFocus['fy'] * 0.72) + ((0.5 + ($jy * 0.38)) * 0.28);
        } else {
            $baseFx = 0.5 + ($jx * 0.36);
            $baseFy = 0.5 + ($jy * 0.34);
        }

        $fx = $this->clamp((float)$baseFx + ($jx * 0.16), $safeMin, $safeMax);
        $fy = $this->clamp((float)$baseFy + ($jy * 0.14), $safeMin, $safeMax);
        return [
            'fx' => $this->clamp((float)$fx, $safeMin, $safeMax),
            'fy' => $this->clamp((float)$fy, $safeMin, $safeMax),
            'zoom_start' => 1.0,
            'zoom_end' => min($maxNoBlurZoom, $nonFaceZoomEnd),
            'duration' => self::PHOTO_DURATION,
        ];
    }

    private function resolveImageDimensions(string $localPath, int $fallbackW, int $fallbackH): array {
        $w = $fallbackW;
        $h = $fallbackH;

        // Prefer Imagick metadata because it can represent orientation reliably for more formats.
        if (class_exists(\Imagick::class)) {
            try {
                $img = new \Imagick();
                $img->readImage($localPath . '[0]');
                $iw = (int)$img->getImageWidth();
                $ih = (int)$img->getImageHeight();
                if ($iw > 0 && $ih > 0) {
                    $w = $iw;
                    $h = $ih;
                }

                $orientation = (int)$img->getImageOrientation();
                if (in_array($orientation, [
                    \Imagick::ORIENTATION_LEFTTOP,
                    \Imagick::ORIENTATION_RIGHTTOP,
                    \Imagick::ORIENTATION_RIGHTBOTTOM,
                    \Imagick::ORIENTATION_LEFTBOTTOM,
                ], true)) {
                    $tmp = $w;
                    $w = $h;
                    $h = $tmp;
                }

                $img->destroy();
            } catch (\Throwable $e) {
                // Fall through to other probes/fallbacks.
            }
        }

        if ($w <= 0 || $h <= 0) {
            $raw = @getimagesize($localPath);
            if (is_array($raw) && !empty($raw[0]) && !empty($raw[1])) {
                $w = (int)$raw[0];
                $h = (int)$raw[1];
            }
        }

        if ($w <= 0 || $h <= 0) {
            $w = max(1, $fallbackW);
            $h = max(1, $fallbackH);
        }

        return ['w' => $w, 'h' => $h];
    }

    private function getOutputDimensions(string $userId): array {
        $orientation = $this->config->getUserValue(
            $userId,
            Application::APP_ID,
            'output_orientation',
            'landscape_16_9',
        );

        return match ($orientation) {
            'portrait_9_16' => [1080, 1920],
            'square_1_1' => [1080, 1080],
            default => [self::WIDTH, self::HEIGHT],
        };
    }

    private function outputWidth(): int {
        return $this->outputWidth;
    }

    private function outputHeight(): int {
        return $this->outputHeight;
    }

    private function targetAspect(): float {
        return $this->outputWidth() / max(1, $this->outputHeight());
    }

    /**
     * Lightweight saliency approximation: pick the highest-detail tile in a dense grid.
     */
    private function detectInterestingFocus(string $localPath): ?array {
        if (!class_exists(\Imagick::class)) {
            return null;
        }

        try {
            $img = new \Imagick();
            $img->readImage($localPath . '[0]');
            $img->thumbnailImage(384, 384, true, true);
            $img->setImageColorspace(\Imagick::COLORSPACE_GRAY);
            $img->edgeImage(1);

            $w = $img->getImageWidth();
            $h = $img->getImageHeight();
            if ($w < 3 || $h < 3) {
                $img->destroy();
                return null;
            }

            $bestScore = -1.0;
            $bestFx = 0.5;
            $bestFy = 0.5;
            $aspect = $h > 0 ? ($w / $h) : 1.0;
            $gridX = 4;
            $gridY = 4;
            if ($aspect > 2.5) {
                $gridX = 6;
                $gridY = 3;
            } elseif ($aspect < 0.4) {
                $gridX = 3;
                $gridY = 6;
            }

            for ($gy = 0; $gy < $gridY; $gy++) {
                for ($gx = 0; $gx < $gridX; $gx++) {
                    $x = (int)floor(($gx * $w) / $gridX);
                    $y = (int)floor(($gy * $h) / $gridY);
                    $x2 = (int)floor((($gx + 1) * $w) / $gridX);
                    $y2 = (int)floor((($gy + 1) * $h) / $gridY);
                    $tw = max(1, $x2 - $x);
                    $th = max(1, $y2 - $y);

                    $tile = clone $img;
                    $tile->cropImage($tw, $th, $x, $y);
                    $stats = $tile->getImageChannelStatistics();
                    $tile->destroy();

                    $stddev = 0.0;
                    foreach ($stats as $channelStats) {
                        if (!is_array($channelStats) || !isset($channelStats['standardDeviation'])) {
                            continue;
                        }
                        $stddev = max($stddev, (float)$channelStats['standardDeviation']);
                    }

                    // Slight center bias to avoid extreme corners unless they are clearly more detailed.
                    $cx = (($x + ($tw / 2.0)) / $w);
                    $cy = (($y + ($th / 2.0)) / $h);
                    $centerBias = 1.0 - (0.15 * sqrt(($cx - 0.5) ** 2 + ($cy - 0.5) ** 2));
                    $score = $stddev * $centerBias;
                    if ($score > $bestScore) {
                        $bestScore = $score;
                        $bestFx = $cx;
                        $bestFy = $cy;
                    }
                }
            }

            $img->destroy();
            return ['fx' => $bestFx, 'fy' => $bestFy];
        } catch (\Throwable $e) {
            return null;
        }
    }

    private function maxNoBlurZoom(int $imageW, int $imageH): float {
        if ($imageW <= 0 || $imageH <= 0) {
            return 1.35;
        }

        $limit = min($imageW / $this->outputWidth(), $imageH / $this->outputHeight());
        return max(1.0, min(2.2, $limit));
    }

    /**
     * Convert a focus point from source-normalized coordinates into the
     * normalized coordinates after cover-scale + center-crop to output frame.
     */
    private function mapFocusToCoverFrame(float $fx, float $fy, int $imageW, int $imageH): array {
        $fx = $this->clamp($fx, 0.0, 1.0);
        $fy = $this->clamp($fy, 0.0, 1.0);

        if ($imageW <= 0 || $imageH <= 0) {
            return ['fx' => $fx, 'fy' => $fy];
        }

        $outW = $this->outputWidth();
        $outH = $this->outputHeight();
        $scale = max($outW / $imageW, $outH / $imageH);
        $scaledW = $imageW * $scale;
        $scaledH = $imageH * $scale;
        $cropX = max(0.0, ($scaledW - $outW) / 2.0);
        $cropY = max(0.0, ($scaledH - $outH) / 2.0);

        $px = ($fx * $scaledW) - $cropX;
        $py = ($fy * $scaledH) - $cropY;

        return [
            'fx' => $this->clamp($px / $outW, 0.0, 1.0),
            'fy' => $this->clamp($py / $outH, 0.0, 1.0),
        ];
    }

    private function mapFaceBoxToCoverFrame(float $faceW, float $faceH, int $imageW, int $imageH): array {
        $faceW = $this->clamp($faceW, 0.0, 1.0);
        $faceH = $this->clamp($faceH, 0.0, 1.0);

        if ($imageW <= 0 || $imageH <= 0) {
            return ['w' => $faceW, 'h' => $faceH];
        }

        $outW = $this->outputWidth();
        $outH = $this->outputHeight();
        $scale = max($outW / $imageW, $outH / $imageH);
        $scaledW = $imageW * $scale;
        $scaledH = $imageH * $scale;

        return [
            'w' => $this->clamp(($faceW * $scaledW) / $outW, 0.0, 1.0),
            'h' => $this->clamp(($faceH * $scaledH) / $outH, 0.0, 1.0),
        ];
    }

    private function pickPrimaryFace(array $faces): array {
        $bestFace = $faces[0] ?? ['w' => 0.0, 'h' => 0.0, 'fx' => 0.5, 'fy' => 0.5];
        $bestScore = -1.0;

        foreach ($faces as $face) {
            $w = max(0.0, (float)($face['w'] ?? 0.0));
            $h = max(0.0, (float)($face['h'] ?? 0.0));
            $score = $w * $h;
            if ($score > $bestScore) {
                $bestScore = $score;
                $bestFace = $face;
            }
        }

        return $bestFace;
    }

    private function isStrongAspectMismatch(float $sourceAspect, float $targetAspect): bool {
        if ($sourceAspect <= 0.0 || $targetAspect <= 0.0) {
            return false;
        }

        $ratio = max($sourceAspect / $targetAspect, $targetAspect / $sourceAspect);
        return $ratio >= 1.65;
    }

    private function easeInOutCubicExpr(string $tExpr): string {
        return "if(lt({$tExpr},0.5),4*pow({$tExpr},3),1-pow(-2*{$tExpr}+2,3)/2)";
    }

    private function clamp(float $value, float $min, float $max): float {
        return max($min, min($max, $value));
    }

    private function getLocalPath(\OCP\Files\Node $node): string {
        $storage = $node->getStorage();

        if (!$storage->isLocal()) {
            throw new \RuntimeException('Reel only supports local storage for rendering');
        }

        $localFile = $storage->getLocalFile($node->getInternalPath());
        if ($localFile === null) {
            throw new \RuntimeException('Could not get local path for ' . $node->getPath());
        }

        return $localFile;
    }

    // -------------------------------------------------------------------------
    // Output folder
    // -------------------------------------------------------------------------

    private function ensureOutputFolder(Folder $userFolder): Folder {
        if ($userFolder->nodeExists(self::OUTPUT_FOLDER)) {
            $node = $userFolder->get(self::OUTPUT_FOLDER);
            if (!$node instanceof Folder) {
                throw new \RuntimeException(self::OUTPUT_FOLDER . ' exists but is not a folder');
            }
            return $node;
        }

        return $userFolder->newFolder(self::OUTPUT_FOLDER);
    }

    private function persistOutputFile(Folder $outputFolder, string $outputName, string $localOutputPath): File {
        $node = $outputFolder->nodeExists($outputName)
            ? $outputFolder->get($outputName)
            : $outputFolder->newFile($outputName);

        if (!$node instanceof File) {
            throw new \RuntimeException($outputName . ' exists but is not a file');
        }

        $in = fopen($localOutputPath, 'rb');
        if ($in === false) {
            throw new \RuntimeException('Failed to open rendered file for import');
        }

        $out = $node->fopen('w');
        if (!is_resource($out)) {
            fclose($in);
            throw new \RuntimeException('Failed to open destination file in Nextcloud storage');
        }

        stream_copy_to_stream($in, $out);
        fclose($in);
        fclose($out);

        return $node;
    }

    private function buildOutputFilename(array $event): string {
        $slug = strtolower(preg_replace('/[^a-zA-Z0-9]+/', '-', $event['title'] ?? 'reel'));
        $slug = trim($slug, '-');
        if (strlen($slug) > 80) {
            $slug = rtrim(substr($slug, 0, 80), '-');
        }
        return 'reel-' . $slug . '-' . $event['id'] . '.mp4';
    }

    private function getMusicPath(?string $theme, string $userId): ?string {
        if (is_string($theme) && str_starts_with($theme, 'custom:')) {
            $customPath = substr($theme, strlen('custom:'));
            if ($customPath !== false && $customPath !== '') {
                return $this->musicService->resolveCustomMusicFilePath($userId, $customPath);
            }
        }

        $genre = $this->normalizeMusicGenre($theme);
        return $this->musicService->getRandomBundledMusicFile($genre);
    }

    private function normalizeMusicGenre(?string $theme): string {
        return match($theme) {
            'acoustic_folk', 'indie_pop', 'cinematic_orchestral' => $theme,
            // Backwards compatibility for old themes.
            'summer' => 'acoustic_folk',
            'minimal' => 'cinematic_orchestral',
            default => 'indie_pop',
        };
    }

    // -------------------------------------------------------------------------
    // FFmpeg execution
    // -------------------------------------------------------------------------

    private function runFfmpeg(array $cmd): void {
        $process = proc_open(
            $cmd,
            [
                0 => ['pipe', 'r'],
                1 => ['pipe', 'w'],
                2 => ['pipe', 'w'],
            ],
            $pipes
        );

        if (!is_resource($process)) {
            throw new \RuntimeException('Failed to start FFmpeg process');
        }

        fclose($pipes[0]);
        stream_set_blocking($pipes[1], false);
        stream_set_blocking($pipes[2], false);

        $startedAt = time();
        $stderr = '';

        while (true) {
            $status = proc_get_status($process);

            $errChunk = stream_get_contents($pipes[2]);
            if ($errChunk !== false && $errChunk !== '') {
                if (strlen($stderr) < self::FFMPEG_STDERR_MAX_BYTES) {
                    $stderr .= substr($errChunk, 0, self::FFMPEG_STDERR_MAX_BYTES - strlen($stderr));
                }
            }

            if (!$status['running']) {
                break;
            }

            if ((time() - $startedAt) > self::FFMPEG_TIMEOUT_SECONDS) {
                proc_terminate($process);
                usleep(200000);
                $status = proc_get_status($process);
                if ($status['running']) {
                    proc_terminate($process, 9);
                }

                fclose($pipes[1]);
                fclose($pipes[2]);
                proc_close($process);

                throw new \RuntimeException('FFmpeg timed out and was terminated');
            }

            usleep(100000);
        }

        // Drain any remaining stderr output once the process exits.
        $errChunk = stream_get_contents($pipes[2]);
        if ($errChunk !== false && $errChunk !== '') {
            if (strlen($stderr) < self::FFMPEG_STDERR_MAX_BYTES) {
                $stderr .= substr($errChunk, 0, self::FFMPEG_STDERR_MAX_BYTES - strlen($stderr));
            }
        }

        fclose($pipes[1]);
        fclose($pipes[2]);
        $exitCode = (int)($status['exitcode'] ?? -1);
        $closeCode = proc_close($process);
        if ($exitCode === -1) {
            $exitCode = $closeCode;
        }

        if ($exitCode !== 0) {
            $this->logger->error('Reel: FFmpeg failed (exit {code}): {stderr}', [
                'code'   => $exitCode,
                'stderr' => $stderr,
            ]);
            throw new \RuntimeException("FFmpeg exited with code {$exitCode}: " . substr($stderr, -500));
        }
    }

