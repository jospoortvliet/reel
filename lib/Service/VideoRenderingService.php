/**
 * VideoRenderingService
 *
 * Renders a highlight video for a Reel event using FFmpeg.
 *
 * Strategy — two-pass rendering:
 *   Pass 1: Normalize every input (photo or video, any codec) to a
 *           temporary 1080p H.264 30fps intermediate clip.
 *   Pass 2: Concatenate all intermediates and encode the final H.265 MP4.
 *
 * This approach handles any input codec/container reliably.
 */
class VideoRenderingService {

    // How long each photo is shown (seconds)
    private const PHOTO_DURATION = 2.5;

    // Maximum duration of a video clip (seconds)
    private const MAX_CLIP_DURATION = 8.0;

    // Output dimensions
    private const WIDTH  = 1920;
    private const HEIGHT = 1080;

    // Frames per second for all intermediates and output
    private const FPS = 30;

    // Where to save generated videos in the user's file tree
    private const OUTPUT_FOLDER = 'Reels';

    private const FFMPEG_TIMEOUT_SECONDS = 1800;
    private const FFMPEG_STDERR_MAX_BYTES = 262144;
    private const TARGET_ASPECT = self::WIDTH / self::HEIGHT;
    private const MUSIC_BASE_VOLUME = 0.50;
    private const MUSIC_DUCKED_VOLUME = 1.00;
    private const MUSIC_FADE_OUT_SECONDS = 2.4;
    private const AUDIO_CROSSFADE_SECONDS = 0.10;
    private const INTRO_TEXT_DURATION_SECONDS = 2.5;
    private const OUTRO_VIDEO_FADE_SECONDS = 3.0;

    private int $outputWidth = self::WIDTH;
    private int $outputHeight = self::HEIGHT;

    public function __construct(
        private IDBConnection   $db,
        private IRootFolder     $rootFolder,
        private LoggerInterface $logger,
        private IConfig         $config,
        private MemoriesRepository $memoriesRepository,
        private MusicService $musicService,
    ) {}

    /**
     * Render a video for the given event ID.
     * Returns the Nextcloud file ID of the generated video.
     */
    public function renderEvent(int $eventId, string $userId, ?callable $onProgress = null, ?callable $onDebug = null): int {
        $this->logger->info('Reel: starting render for event {id}', ['id' => $eventId]);

        [$this->outputWidth, $this->outputHeight] = $this->getOutputDimensions($userId);

        $event = $this->loadEvent($eventId, $userId);
        $media = $this->loadIncludedMedia($eventId, $userId);

        if (count($media) < 2) {
            throw new \RuntimeException("Event {$eventId} has fewer than 2 included media items");
        }

        $userFolder = $this->rootFolder->getUserFolder($userId);
        $items      = $this->resolveFilePaths($media, $userFolder, $userId, $onDebug);

        if (count($items) < 2) {
            throw new \RuntimeException("Could not resolve enough media paths for event {$eventId}");
        }

        $tempDir = sys_get_temp_dir() . '/reel_' . $eventId . '_' . uniqid('', true);
        mkdir($tempDir, 0700, true);
        $renderSucceeded = false;

        try {
            // Pass 1: normalize each item to an intermediate clip
            $clips = $this->normalizeItems($items, $tempDir, $onProgress, $onDebug);

            // Pass 2: concatenate intermediates into final output
            if ($onProgress) { $onProgress(90); }
            $outputFolder    = $this->ensureOutputFolder($userFolder);
            $outputName      = $this->buildOutputFilename($event);
            $localOutputPath = $tempDir . '/final.mp4';

                $this->concatenateIntermediates(
                    $clips,
                    $localOutputPath,
                    $event,
                    $event['theme'] ?? null,
                    $userId,
                    $onDebug,
                );

            // Persist generated bytes via Nextcloud file API.
            $outputFile = $this->persistOutputFile($outputFolder, $outputName, $localOutputPath);
            $fileId     = $outputFile->getId();

            $this->updateEventVideoFileId($eventId, $userId, $fileId);

            $this->logger->info('Reel: render complete for event {id}, file {fileId}', [
                'id'     => $eventId,
                'fileId' => $fileId,
            ]);

            $renderSucceeded = true;

            return $fileId;

        } finally {
            if ($onDebug) {
                $onDebug('Debug mode: preserving temp dir ' . $tempDir);
            } else {
                $this->cleanupTempDir($tempDir);
            }
        }
    }

    // -------------------------------------------------------------------------
    // Pass 1: Normalize items to intermediates
    // -------------------------------------------------------------------------

    /**
     * Converts each item (photo or video, any format) to a normalized
     * H.264 1080p 30fps MP4 intermediate clip.
     */
    private function normalizeItems(array $items, string $tempDir, ?callable $onProgress = null, ?callable $onDebug = null): array {
        $clips = [];
        $total = count($items);

        for ($index = 0; $index < $total; $index++) {
            $item = $items[$index];
            $isLastItem = ($index === ($total - 1));
            $clipDuration = (float)($item['duration'] ?? 0.0);

            // Keep default stills at 2.5s, but give the final still extra hold time
            // so the end fade can run after ~1s unobstructed viewing.
            if (!$item['is_video'] && $isLastItem) {
                $clipDuration = max($clipDuration, 4.0);
            }

            if ($this->canBuildTriptychSegment($items, $index)) {
                $a = $items[$index];
                $b = $items[$index + 1];
                $c = $items[$index + 2];
                $outPath = $tempDir . '/clip_' . str_pad((string)$index, 4, '0', STR_PAD_LEFT) . '_triptych.mp4';
                $dirSeed = abs((int)crc32((string)$a['file_id'])) % 2;
                $dirs = $dirSeed === 0 ? ['up', 'down', 'up'] : ['down', 'up', 'down'];

                $triptychA = $this->prepareStillInputForLoop((string)$a['path'], $tempDir, $index, 'a');
                $triptychB = $this->prepareStillInputForLoop((string)$b['path'], $tempDir, $index, 'b');
                $triptychC = $this->prepareStillInputForLoop((string)$c['path'], $tempDir, $index, 'c');

                $cmd = $this->buildTriptychNormalizeCmd(
                    $triptychA,
                    $triptychB,
                    $triptychC,
                    $outPath,
                    3.2,
                    $dirs,
                );

                if ($onDebug) {
                    $onDebug('Triptych segment: files=' . basename((string)$a['path']) . ',' . basename((string)$b['path']) . ',' . basename((string)$c['path']) . ' dirs=' . implode('/', $dirs));
                    $onDebug("FFmpeg cmd: " . implode(' ', array_map('escapeshellarg', $cmd)));
                }
                $this->runFfmpeg($cmd);

                $clips[] = [
                    'path' => $outPath,
                    'duration' => 3.2,
                    'kind' => 'triptych',
                    'is_breaker' => true,
                    'motion' => 'triptych',
                    'run_state' => 'triptych',
                    'breaker_type' => 'triptych',
                ];

                if ($onProgress) {
                    $progress = (int)(1 + ((min($total, $index + 3)) / $total) * 88);
                    $onProgress($progress);
                }

                $index += 2;
                continue;
            }

            $outPath = $tempDir . '/clip_' . str_pad((string)$index, 4, '0', STR_PAD_LEFT) . '.mp4';

            $inputPath = $item['path'];

            // HEIC/AVIF: convert to a temp JPEG first since FFmpeg's HEIC
            // demuxer doesn't support -loop 1 and the loop video filter
            // produces black frames on this FFmpeg build.
            $tempJpeg = null;
            if (!$item['is_video']) {
                $ext = strtolower(pathinfo($inputPath, PATHINFO_EXTENSION));
                if (in_array($ext, ['heic', 'heif', 'avif'], true)) {
                    $tempJpeg = $tempDir . '/thumb_' . $index . '.jpg';
                    if ($onDebug) { $onDebug("HEIC→JPEG: {$inputPath} → {$tempJpeg}"); }
                    $this->convertToJpeg($inputPath, $tempJpeg);
                    $inputPath = $tempJpeg;
                }
            }

            $cmd = $item['is_video']
                ? $this->buildVideoNormalizeCmd($inputPath, $outPath, $clipDuration, (float)($item['start_at'] ?? 0.0), (int)($item['w'] ?? 0), (int)($item['h'] ?? 0), 'apple_subtle')
                : $this->buildPhotoNormalizeCmd($inputPath, $outPath, $clipDuration, $item['motion']);

            $this->logger->debug('Reel: normalizing item {i}/{total}', [
                'i'     => $index + 1,
                'total' => $total,
            ]);

            if ($onDebug) { $onDebug("FFmpeg cmd: " . implode(' ', array_map('escapeshellarg', $cmd))); }
            $this->runFfmpeg($cmd);
            $clips[] = [
                'path' => $outPath,
                'duration' => $clipDuration,
                'kind' => $item['is_video'] ? 'video' : 'photo',
                'is_breaker' => (bool)$item['is_video'],
                'motion' => (string)($item['motion']['run_motion'] ?? 'none'),
                'run_state' => $item['is_video'] ? 'video' : (string)($item['motion']['run_motion'] ?? 'none'),
                'breaker_type' => $item['is_video'] ? 'video' : 'none',
            ];

            // Report progress: clips take up 1-89%, Pass 2 gets 90-99%
            if ($onProgress) {
                $progress = (int)(1 + (($index + 1) / $total) * 88);
                $onProgress($progress);
            }
        }

        return $clips;
    }

    private function canBuildTriptychSegment(array $items, int $startIndex): bool {
        if (!isset($items[$startIndex + 2])) {
            return false;
        }

        for ($i = $startIndex; $i <= $startIndex + 2; $i++) {
            $it = $items[$i];
            if (!empty($it['is_video'])) {
                return false;
            }

            $w = (int)($it['w'] ?? 0);
            $h = (int)($it['h'] ?? 0);
            if ($w <= 0 || $h <= 0 || $w >= $h) {
                return false;
            }
        }

        return true;
    }

    /**
     * Normalize a video clip to standard intermediate format.
     * Handles any input codec/container.
     */
    private function buildVideoNormalizeCmd(string $inputPath, string $outputPath, float $duration, float $startAt = 0.0, int $videoW = 0, int $videoH = 0, string $motionStyle = 'apple_subtle'): array {
        $cmd = [
            'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
            '-i', $inputPath,
            '-ignore_unknown',  // don't puke out on unknown audio or video streams
        ];

        if ($startAt > 0.0) {
            $cmd[] = '-ss';
            $cmd[] = number_format($startAt, 3, '.', '');
        }

        // For apple_subtle, use a blurred fill background instead of black bars
        // when the video aspect ratio differs from the output (e.g. portrait video
        // in a landscape frame, or landscape video in a portrait frame).
        if ($motionStyle === 'apple_subtle' && $videoW > 0 && $videoH > 0) {
            $outW = $this->outputWidth();
            $outH = $this->outputHeight();
            $targetAspect = $this->targetAspect();
            $videoAspect  = $videoW / $videoH;
            $aspectDiff   = abs($videoAspect - $targetAspect) / $targetAspect;

            if ($aspectDiff > 0.02) {
                // Fit the video into the output frame (no crop), letterbox/pillarbox.
                if ($videoAspect >= $targetAspect) {
                    $fitW = $outW;
                    $fitH = (int)(round($outW / $videoAspect / 2) * 2);
                } else {
                    $fitH = $outH;
                    $fitW = (int)(round($outH * $videoAspect / 2) * 2);
                }
                $overlayX = (int)(($outW - $fitW) / 2);
                $overlayY = (int)(($outH - $fitH) / 2);

                $fc = implode(';', [
                    '[0:v]split=2[bgsrc][fgsrc]',
                    "[bgsrc]scale=w={$outW}:h={$outH}:force_original_aspect_ratio=increase,crop={$outW}:{$outH},gblur=sigma=36,eq=saturation=0.92:brightness=-0.02[bg]",
                    "[fgsrc]scale=w={$fitW}:h={$fitH}:flags=lanczos[fg]",
                    "[bg][fg]overlay=x={$overlayX}:y={$overlayY},setsar=1[vout]",
                ]);

                return array_merge($cmd, [
                    '-t', number_format($duration, 3, '.', ''),
                    '-filter_complex', $fc,
                    '-map', '[vout]',
                    '-map', '0:a:0?',     // first audio stream only, if exists
                    '-af', 'aformat=sample_rates=44100:channel_layouts=stereo,asetpts=PTS-STARTPTS',
                    '-r', (string)self::FPS,
                    '-c:v', 'libx264',
                    '-preset', 'fast',
                    '-crf', '18',
                    '-c:a', 'aac',
                    '-ar', '44100',
                    '-ac', '2',
                    '-movflags', '+faststart',
                    $outputPath,
                ]);
            }
        }

        return array_merge($cmd, [
            '-t', number_format($duration, 3, '.', ''),
            '-vf', $this->scaleFilter(),
            '-map', '0:v',
            '-map', '0:a:0?',     // first audio stream only, if exists
            '-af', 'aformat=sample_rates=44100:channel_layouts=stereo,asetpts=PTS-STARTPTS',
            '-r', (string)self::FPS,
            '-c:v', 'libx264',
            '-preset', 'fast',
            '-crf', '18',           // high quality intermediate
            '-c:a', 'aac',
            '-ar', '44100',
            '-ac', '2',
            '-movflags', '+faststart',
            $outputPath,
        ]);
    }

    /**
     * Normalize a photo to a short video clip with a subtle Ken Burns zoom.
     * Adds a silent audio track so all intermediates are uniform (video+audio).
     */
    private function buildPhotoNormalizeCmd(string $inputPath, string $outputPath, float $duration, array $motion): array {
        $frameCount = (int)ceil($duration * self::FPS);

        $style = (string)($motion['style'] ?? 'apple_subtle');
        $zoomStartRaw = (float)($motion['zoom_start'] ?? 1.0);
        $zoomEndRaw = (float)($motion['zoom_end'] ?? 1.0);
        $zoomStart = number_format($zoomStartRaw, 4, '.', '');
        $zoomEnd = number_format($zoomEndRaw, 4, '.', '');
        $fx = number_format((float)$motion['fx'], 4, '.', '');
        $fy = number_format((float)($motion['fy'] ?? 0.5), 4, '.', '');
        $mode = (string)($motion['mode'] ?? 'zoom');
        $framesMinusOne = max(1, $frameCount - 1);

        // Cover scale preserves aspect ratio exactly without warping.
        // For pan modes we intentionally pan on the uncropped scaled frame,
        // otherwise movement range is tiny after center-crop.
        $outW = $this->outputWidth();
        $outH = $this->outputHeight();
        $targetAspect = $this->targetAspect();
        $coverScale = "scale=w='if(gte(iw/ih," . $targetAspect . "),-2," . $outW . ")':h='if(gte(iw/ih," . $targetAspect . ")," . $outH . ",-2)'";
        $coverCrop = 'crop=' . $outW . ':' . $outH . ':(iw-' . $outW . ')/2:(ih-' . $outH . ')/2';

        $t = '(n/' . $framesMinusOne . ')';
        $zoomEase = $this->easeInOutCubicExpr($t);

        if ($style === 'apple_subtle') {
            // Clamp zoom range to subtle values.
            $appleZoomStart = $this->clamp($zoomStartRaw, 0.98, 1.06);
            $appleZoomEnd   = $this->clamp($zoomEndRaw,   0.98, 1.06);

            // Compute the fit dimensions: the pixel box the photo occupies when
            // letterboxed/pillarboxed into the output frame (no cropping).
            // Dimensions must be even (required by libx264).
            $imageAspect = (float)($motion['image_aspect'] ?? $targetAspect);
            if ($imageAspect >= $targetAspect) {
                $fitW = $outW;
                $fitH = (int)(round($outW / $imageAspect / 2) * 2);
            } else {
                $fitH = $outH;
                $fitW = (int)(round($outH * $imageAspect / 2) * 2);
            }

            // zoompan: input is pre-scaled to fitW×fitH; output is always fitW×fitH.
            // zoom=1.0 → shows the full fitW×fitH (no zoom), zoom>1 → zoomed in.
            // The overlay onto the blurred background is always at a fixed position.
            $appleZoomStartFmt = number_format($appleZoomStart, 4, '.', '');
            $appleZoomEndFmt   = number_format($appleZoomEnd,   4, '.', '');

            // Per-frame zoom interpolation.
            $zpZoom = "{$appleZoomStartFmt}+({$appleZoomEndFmt}-{$appleZoomStartFmt})*{$zoomEase}";

            $focusX = number_format($this->clamp((float)($motion['fx'] ?? 0.5), 0.12, 0.88), 4, '.', '');
            $focusY = number_format($this->clamp((float)($motion['fy'] ?? 0.5), 0.12, 0.88), 4, '.', '');

            // Overlay is constant: fit box always centered in output frame.
            $overlayX = (int)(($outW - $fitW) / 2);
            $overlayY = (int)(($outH - $fitH) / 2);

            $fitScale = "scale=w={$fitW}:h={$fitH}:flags=lanczos";
            // Animated scale + crop avoids zoompan coordinate stepping jitter.
            $animatedScale = "scale=w='trunc(iw*({$zpZoom})/2)*2':h='trunc(ih*({$zpZoom})/2)*2':flags=lanczos:eval=frame";
            // Keep zoom centered on the focus point, then clamp to image bounds.
            $cropX = "max(0,min(iw-{$fitW},iw*({$focusX})-{$fitW}/2))";
            $cropY = "max(0,min(ih-{$fitH},ih*({$focusY})-{$fitH}/2))";
            $animatedCrop = "crop={$fitW}:{$fitH}:'{$cropX}':'{$cropY}'";

            $fc = implode(';', [
                "[0:v]split=2[bgsrc][fgsrc]",
                "[bgsrc]scale=w={$outW}:h={$outH}:force_original_aspect_ratio=increase,crop={$outW}:{$outH},gblur=sigma=36,eq=saturation=0.92:brightness=-0.02[bg]",
                "[fgsrc]{$fitScale},{$animatedScale},{$animatedCrop}[fg]",
                "[bg][fg]overlay=x={$overlayX}:y={$overlayY},setsar=1[vout]",
            ]);

            return [
                'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
                '-loop', '1',
                '-i', $inputPath,
                '-f', 'lavfi', '-i', 'anullsrc=channel_layout=stereo:sample_rate=44100',
                '-t', number_format($duration, 2, '.', ''),
                '-filter_complex', $fc,
                '-map', '[vout]',
                '-map', '1:a',
                '-r', (string)self::FPS,
                '-c:v', 'libx264',
                '-preset', 'fast',
                '-crf', '18',
                '-c:a', 'aac',
                '-ar', '44100',
                '-ac', '2',
                '-shortest',
                '-movflags', '+faststart',
                $outputPath,
            ];
        }

        // For explicit pan modes, animate crop directly over the full clip duration.
        // This avoids zoompan timing quirks with looped still-image inputs.
        if ($mode === 'pan_x') {
            $fxStart = number_format((float)($motion['fx_start'] ?? 0.08), 4, '.', '');
            $fxEnd = number_format((float)($motion['fx_end'] ?? 0.92), 4, '.', '');
            $fyFixed = number_format((float)($motion['fy'] ?? 0.5), 4, '.', '');
            $panT = '(n/' . $framesMinusOne . ')';
            $panEase = $this->easeInOutCubicExpr($panT);
            $fxExpr = "{$fxStart}+({$fxEnd}-{$fxStart})*{$panEase}";
            $cropX = "max(0,min(iw-" . $outW . ",(iw-" . $outW . ")*({$fxExpr})))";
            $cropY = "max(0,min(ih-" . $outH . ",(ih-" . $outH . ")*({$fyFixed})))";
            $panCrop = "crop=" . $outW . ":" . $outH . ":'{$cropX}':'{$cropY}'";
            $vf = implode(',', [$coverScale, $panCrop, 'setsar=1']);

            return [
                'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
                '-loop', '1',
                '-i', $inputPath,
                '-f', 'lavfi', '-i', 'anullsrc=channel_layout=stereo:sample_rate=44100',
                '-t', number_format($duration, 2, '.', ''),
                '-vf', $vf,
                '-r', (string)self::FPS,
                '-c:v', 'libx264',
                '-preset', 'fast',
                '-crf', '18',
                '-c:a', 'aac',
                '-ar', '44100',
                '-ac', '2',
                '-shortest',
                '-movflags', '+faststart',
                $outputPath,
            ];
        }

        if ($mode === 'pan_y') {
            $fyStart = number_format((float)($motion['fy_start'] ?? 0.08), 4, '.', '');
            $fyEnd = number_format((float)($motion['fy_end'] ?? 0.92), 4, '.', '');
            $fxFixed = number_format((float)($motion['fx'] ?? 0.5), 4, '.', '');
            $panT = '(n/' . $framesMinusOne . ')';
            $panEase = $this->easeInOutCubicExpr($panT);
            $fyExpr = "{$fyStart}+({$fyEnd}-{$fyStart})*{$panEase}";
            $cropX = "max(0,min(iw-" . $outW . ",(iw-" . $outW . ")*({$fxFixed})))";
            $cropY = "max(0,min(ih-" . $outH . ",(ih-" . $outH . ")*({$fyExpr})))";
            $panCrop = "crop=" . $outW . ":" . $outH . ":'{$cropX}':'{$cropY}'";
            $vf = implode(',', [$coverScale, $panCrop, 'setsar=1']);

            return [
                'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
                '-loop', '1',
                '-i', $inputPath,
                '-f', 'lavfi', '-i', 'anullsrc=channel_layout=stereo:sample_rate=44100',
                '-t', number_format($duration, 2, '.', ''),
                '-vf', $vf,
                '-r', (string)self::FPS,
                '-c:v', 'libx264',
                '-preset', 'fast',
                '-crf', '18',
                '-c:a', 'aac',
                '-ar', '44100',
                '-ac', '2',
                '-shortest',
                '-movflags', '+faststart',
                $outputPath,
            ];
        }

        $zoomExpr = "{$zoomStart}+({$zoomEnd}-{$zoomStart})*{$zoomEase}";
        // Animated scale + crop avoids zoompan's integer coordinate stepping jitter.
        $animatedScale = "scale=w='trunc(iw*({$zoomExpr})/2)*2':h='trunc(ih*({$zoomExpr})/2)*2':flags=lanczos:eval=frame";
        $cropX = "max(0,min(iw-" . $outW . ",(iw-" . $outW . ")*({$fx})))";
        $cropY = "max(0,min(ih-" . $outH . ",(ih-" . $outH . ")*({$fy})))";
        $animatedCrop = "crop=" . $outW . ":" . $outH . ":'{$cropX}':'{$cropY}'";

        $vf = implode(',', [$coverScale, $animatedScale, $animatedCrop, 'setsar=1']);

        return [
            'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
            '-loop', '1',
            '-i', $inputPath,
            '-f', 'lavfi', '-i', 'anullsrc=channel_layout=stereo:sample_rate=44100',
            '-t', number_format($duration, 2, '.', ''),
            '-vf', $vf,
            '-r', (string)self::FPS,
            '-c:v', 'libx264',
            '-preset', 'fast',
            '-crf', '18',
            '-c:a', 'aac',
            '-ar', '44100',
            '-ac', '2',
            '-shortest',
            '-movflags', '+faststart',
            $outputPath,
        ];
    }

    /**
     * Convert a HEIC/AVIF image to JPEG using Imagick.
     * This is necessary because FFmpeg's HEIC demuxer doesn't support -loop 1.
     */
    private function convertToJpeg(string $inputPath, string $outputPath): void {
        $imagick = new \Imagick();
        $imagick->readImage($inputPath . '[0]'); // [0] = first frame only
        $imagick->setImageFormat('jpeg');
        $imagick->setImageCompressionQuality(92);
        $imagick->writeImage($outputPath);
        $imagick->clear();
        $imagick->destroy();
    }

    /**
     * Scale + letterbox filter to fit any input into 1080p with black bars.
     */
    private function scaleFilter(): string {
        $outW = $this->outputWidth();
        $outH = $this->outputHeight();
        return "scale=w=" . $outW . ":h=" . $outH
            . ":force_original_aspect_ratio=decrease"
            . ",pad=" . $outW . ":" . $outH
            . ":(ow-iw)/2:(oh-ih)/2:black"
            . ",setsar=1";
    }

    // -------------------------------------------------------------------------
    // Pass 2: Concatenate with transitions → H.264 intermediate
    // Pass 3: Final encode with music, titles, fade → H.265 output
    // -------------------------------------------------------------------------
 
    private function concatenateIntermediates(array $clips, string $outputPath, array $event, ?string $theme, string $userId, ?callable $onDebug = null): void {
        $count = count($clips);
 
        if ($count === 0) {
            throw new \RuntimeException('No clips to concatenate');
        }
 
        if ($count === 1) {
            // Single clip: skip transition pass, go straight to final encode
            $this->runFinalEncodePass(
                $clips[0]['path'],
                (float)$clips[0]['duration'],
                $outputPath,
                $event,
                $theme,
                $userId,
                $onDebug,
            );
            return;
        }
 
        // Pass 2: transitions only → H.264 stitched (low memory, no music/x265)
        $tempDir = dirname($outputPath);
        $stitchedPath = $tempDir . '/stitched.mp4';
 
        $this->runTransitionStitchPass($clips, $stitchedPath, $onDebug);
 
        // Pass 3: final encode with music, titles, fade → H.265
        $totalDuration = $this->computeTimelineDuration($clips);
        $this->runFinalEncodePass(
            $stitchedPath,
            $totalDuration,
            $outputPath,
            $event,
            $theme,
            $userId,
            $onDebug,
        );
    }
        /**
     * Pass 2: stitch all intermediate clips together with xfade transitions.
     *
     * Uses a binary merge tree (tournament bracket) so each clip is re-encoded
     * at most ceil(log2(n)) times rather than n times. For 25 clips that's 5
     * generations instead of 24, and for 130 clips it's 7 instead of 129.
     *
     * Memory is bounded: each FFmpeg call has exactly 2 inputs.
     * Quality loss is bounded: at most ceil(log2(n)) lossy re-encodes per frame.
     *
     * Each "segment" in the working set carries:
     *   path      — path to the encoded file
     *   duration  — total duration of the encoded file
     *   clips     — the original $clips[] entries it was built from, in order
     *               (needed to pick the correct transition at the join point)
     */
    private function runTransitionStitchPass(array $clips, string $outputPath, ?callable $onDebug = null): void {
        $count = count($clips);
        $tempDir = dirname($outputPath);
        $stepCounter = 0;
 
        // Initialise the working set: each clip is its own segment.
        $segments = [];
        foreach ($clips as $clip) {
            $segments[] = [
                'path'     => (string)$clip['path'],
                'duration' => (float)$clip['duration'],
                'clips'    => [$clip],
                'owned'    => false,   // original clip files are not ours to delete
            ];
        }
 
        // Merge pairs until one segment remains.
        while (count($segments) > 1) {
            $nextRound = [];
            $segCount  = count($segments);
 
            for ($j = 0; $j < $segCount; $j += 2) {
                if ($j + 1 >= $segCount) {
                    // Odd one out — carry it forward unchanged.
                    $nextRound[] = $segments[$j];
                    continue;
                }
 
                $left  = $segments[$j];
                $right = $segments[$j + 1];
 
                // The transition at the join point is determined by the last clip
                // of the left segment and the first clip of the right segment.
                // We use the original clip index in the global array for pickTransitionSpec.
                $leftLastClip  = end($left['clips']);
                $rightFirstClip = reset($right['clips']);
                $globalRightIndex = array_search($rightFirstClip, $clips, true);
                if ($globalRightIndex === false) {
                    // Fallback: use count of left clips as a proxy index.
                    $globalRightIndex = count($left['clips']);
                }
 
                $transitionSpec     = $this->pickTransitionSpec($leftLastClip, $rightFirstClip, (int)$globalRightIndex);
                $transitionDuration = (float)$transitionSpec['duration'];
                $offset             = max(0.0, $left['duration'] - $transitionDuration);
 
                $stepCounter++;
                $isFinalMerge = (count($segments) === 2 && $j === 0);
                $mergedPath   = $isFinalMerge
                    ? $outputPath
                    : $tempDir . '/merge_' . $stepCounter . '.mp4';
 
                $filterParts = [
                    '[0:v]settb=AVTB,format=yuv420p[va]',
                    '[1:v]settb=AVTB,format=yuv420p[vb]',
                ];
 
                if (($transitionSpec['type'] ?? '') === 'triptych_strip') {
                    $invert    = !empty($transitionSpec['invert']);
                    $leftDir   = $invert ? 'wiperight' : 'wipeleft';
                    $midDir    = $invert ? 'wipeleft'  : 'wiperight';
                    $offsetStr = number_format($offset, 3, '.', '');
 
                    $filterParts[] = '[va]split=3[a0][a1][a2]';
                    $filterParts[] = '[vb]split=3[b0][b1][b2]';
                    $filterParts[] = '[a0]crop=iw/3:ih:0:0[sa0]';
                    $filterParts[] = '[a1]crop=iw/3:ih:iw/3:0[sa1]';
                    $filterParts[] = '[a2]crop=iw/3:ih:2*iw/3:0[sa2]';
                    $filterParts[] = '[b0]crop=iw/3:ih:0:0[sb0]';
                    $filterParts[] = '[b1]crop=iw/3:ih:iw/3:0[sb1]';
                    $filterParts[] = '[b2]crop=iw/3:ih:2*iw/3:0[sb2]';
                    $filterParts[] = "[sa0][sb0]xfade=transition={$leftDir}:duration={$transitionDuration}:offset={$offsetStr}[x0]";
                    $filterParts[] = "[sa1][sb1]xfade=transition={$midDir}:duration={$transitionDuration}:offset={$offsetStr}[x1]";
                    $filterParts[] = "[sa2][sb2]xfade=transition={$leftDir}:duration={$transitionDuration}:offset={$offsetStr}[x2]";
                    $filterParts[] = '[x0][x1][x2]hstack=inputs=3[vout]';
                } else {
                    $transition  = (string)$transitionSpec['name'];
                    $filterParts[] = "[va][vb]xfade=transition={$transition}"
                        . ':duration=' . number_format($transitionDuration, 3, '.', '')
                        . ':offset='   . number_format($offset, 3, '.', '') . '[vout]';
                }
 
                // Audio crossfade at the join point.
                $leftAudioDur  = max(0.05, $left['duration']  - ($transitionDuration / 2.0));
                $rightAudioDur = max(0.05, $right['duration'] - ($transitionDuration / 2.0));
                $crossfade     = max(0.02, min(self::AUDIO_CROSSFADE_SECONDS,
                    min($leftAudioDur, $rightAudioDur) * 0.45));
 
                $filterParts[] = '[0:a]aformat=sample_rates=44100:channel_layouts=stereo'
                    . ',atrim=duration=' . number_format($leftAudioDur, 3, '.', '')
                    . ',asetpts=PTS-STARTPTS[al]';
                $filterParts[] = '[1:a]aformat=sample_rates=44100:channel_layouts=stereo'
                    . ',atrim=duration=' . number_format($rightAudioDur, 3, '.', '')
                    . ',asetpts=PTS-STARTPTS[ar]';
                $filterParts[] = '[al][ar]acrossfade=d='
                    . number_format($crossfade, 3, '.', '') . ':o=0:c1=tri:c2=tri[aout]';
 
                $cmd = [
                    'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
                    '-i', $left['path'],
                    '-i', $right['path'],
                    '-filter_complex', implode(';', $filterParts),
                    '-map', '[vout]',
                    '-map', '[aout]',
                    '-c:v', 'libx264',
                    '-preset', 'fast',
                    '-crf', '16',
                    '-c:a', 'aac',
                    '-ar', '44100',
                    '-ac', '2',
                    '-movflags', '+faststart',
                    $mergedPath,
                ];
 
                if ($onDebug) {
                    $leftLabel  = basename($left['path']);
                    $rightLabel = basename($right['path']);
                    $onDebug("FFmpeg merge step {$stepCounter} ({$leftLabel} + {$rightLabel}): "
                        . implode(' ', array_map('escapeshellarg', $cmd)));
                }
 
                $this->runFfmpeg($cmd);
 
                // Clean up temp files we own (not original clip files).
                if ($left['owned'])  { @unlink($left['path']); }
                if ($right['owned']) { @unlink($right['path']); }
 
                $mergedDuration = $left['duration'] + $right['duration'] - $transitionDuration;
 
                $nextRound[] = [
                    'path'     => $mergedPath,
                    'duration' => $mergedDuration,
                    'clips'    => array_merge($left['clips'], $right['clips']),
                    'owned'    => true,
                ];
            }
 
            $segments = $nextRound;
        }
 
        $this->logger->info('Reel: transition stitch pass complete ({n} merge steps)', ['n' => $stepCounter]);
    }

    /**
     * Pass 3: take a single stitched video, add titles, music, outro fade, encode H.265.
     * Only 1 video input → x265 lookahead is tiny, peak memory is minimal.
     */
    private function runFinalEncodePass(
        string $stitchedPath,
        float $timelineDuration,
        string $outputPath,
        array $event,
        ?string $theme,
        string $userId,
        ?callable $onDebug = null,
    ): void {
        $musicPath = $this->getMusicPath($theme, $userId);
        $hasMusic  = $musicPath && file_exists($musicPath);
 
        $cmd = ['ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
            '-i', $stitchedPath,
        ];
        if ($hasMusic) {
            $cmd[] = '-stream_loop'; $cmd[] = '-1';
            $cmd[] = '-i'; $cmd[] = $musicPath;
        }
 
        $filterParts = [];
        $videoLabel  = '0:v';
 
        // Title overlays
        $introTitle = trim((string)($event['title'] ?? ''));
        $introDate  = '';
        if (!empty($event['date_start'])) {
            $dt = (new \DateTimeImmutable('@' . (int)$event['date_start']))
                ->setTimezone(new \DateTimeZone(date_default_timezone_get()));
            $introDate = $dt->format('F j, Y');
        }
 
        if ($introTitle !== '') {
            $titleEsc   = $this->escapeDrawtext($introTitle);
            $enableExpr = "lte(t," . number_format(self::INTRO_TEXT_DURATION_SECONDS, 3, '.', '') . ")";
            $filterParts[] = "[{$videoLabel}]drawtext=font=Sans:text='{$titleEsc}':fontcolor=white"
                . ":fontsize=64:borderw=3:bordercolor=black:x=(w-text_w)/2:y=(h*5/6-text_h-8)"
                . ":enable='{$enableExpr}'[vtitle]";
            $videoLabel = 'vtitle';
        }
 
        if ($introDate !== '') {
            $dateEsc    = $this->escapeDrawtext($introDate);
            $enableExpr = "lte(t," . number_format(self::INTRO_TEXT_DURATION_SECONDS, 3, '.', '') . ")";
            $filterParts[] = "[{$videoLabel}]drawtext=font=Sans:text='{$dateEsc}':fontcolor=white"
                . ":fontsize=40:borderw=3:bordercolor=black:x=(w-text_w)/2:y=(h*5/6+8)"
                . ":enable='{$enableExpr}'[vdate]";
            $videoLabel = 'vdate';
        }
 
        // Outro fade to black — freeze-extend tail if needed so fade has room
        $outroFadeDuration = self::OUTRO_VIDEO_FADE_SECONDS;
        $fadeStart         = max(0.0, $timelineDuration - $outroFadeDuration);
        $tailHold          = max(0.0, ($fadeStart + $outroFadeDuration) - $timelineDuration);
 
        if ($tailHold > 0.0) {
            $filterParts[] = "[{$videoLabel}]tpad=stop_mode=clone:stop_duration="
                . number_format($tailHold, 3, '.', '') . "[vhold]";
            $videoLabel = 'vhold';
        }
 
        $filterParts[] = "[{$videoLabel}]fade=t=out:st=" . number_format($fadeStart, 3, '.', '')
            . ":d=" . number_format($outroFadeDuration, 3, '.', '') . "[voutro]";
        $videoLabel = 'voutro';
 
        $totalDuration = $fadeStart + $outroFadeDuration;
 
        // Music + natural audio with sidechain ducking
        $audioLabel = '0:a';
        if ($hasMusic) {
            $musicIdx       = 1;
            $fadeDuration   = min(self::OUTRO_VIDEO_FADE_SECONDS, $totalDuration);
            $musicFadeStart = max(0.0, $totalDuration - $fadeDuration);
            $filterParts[] = "[{$musicIdx}:a]aresample=async=1:first_pts=0"
                . ",atrim=duration=" . number_format($totalDuration, 3, '.', '')
                . ",volume=" . self::MUSIC_BASE_VOLUME
                . ",afade=t=out:st=" . number_format($musicFadeStart, 3, '.', '')
                . ":d=" . number_format($fadeDuration, 3, '.', '') . "[music]";
            $filterParts[] = "[0:a]asplit=2[anat_mix][anat_sc]";
            $filterParts[] = "[music][anat_sc]sidechaincompress=threshold=0.03:ratio=7:attack=20:release=250[ducked]";
            $filterParts[] = "[ducked][anat_mix]amix=inputs=2:duration=longest:dropout_transition=0"
                . ":weights=" . self::MUSIC_DUCKED_VOLUME . " 1:normalize=0[aout]";
            $audioLabel = 'aout';
        }
 
        if (!empty($filterParts)) {
            $cmd[] = '-filter_complex';
            $cmd[] = implode(';', $filterParts);
        }
 
        $cmd[] = '-map'; $cmd[] = "[{$videoLabel}]";
        $cmd[] = '-map'; $cmd[] = $hasMusic ? "[{$audioLabel}]" : "0:a";
        $cmd   = array_merge($cmd, [
            '-c:v', 'libx265',
            '-crf', '23',
            '-preset', 'fast',
            '-tag:v', 'hvc1',
            '-c:a', 'aac',
            '-b:a', '192k',
            '-movflags', '+faststart',
            $outputPath,
        ]);
 
        $this->logger->info('Reel: running final encode pass (duration={d}s)', [
            'd' => number_format($totalDuration, 1),
        ]);
        if ($onDebug) {
            $onDebug("FFmpeg final encode cmd: " . implode(' ', array_map('escapeshellarg', $cmd)));
        }
        $this->runFfmpeg($cmd);
    }
 
    private function computeTimelineDuration(array $clips): float {
        $count = count($clips);
        if ($count === 0) {
            return 0.0;
        }
 
        $accumulated = (float)$clips[0]['duration'];
        for ($i = 1; $i < $count; $i++) {
            $spec = $this->pickTransitionSpec($clips[$i - 1], $clips[$i], $i);
            $accumulated += (float)$clips[$i]['duration'] - (float)$spec['duration'];
        }
        return $accumulated;
    }

    private function escapeDrawtext(string $text): string {
        $escaped = str_replace('\\', '\\\\', $text);
        $escaped = str_replace(':', '\\:', $escaped);
        $escaped = str_replace("'", "\\'", $escaped);
        return str_replace('%', '\\%', $escaped);
    }

    private function pickTransitionSpec(array $prevClip, array $nextClip, int $index): array {
        $prevKind = (string)($prevClip['kind'] ?? 'photo');
        $nextKind = (string)($nextClip['kind'] ?? 'photo');
        $prevBreakerType = (string)($prevClip['breaker_type'] ?? (!empty($prevClip['is_breaker']) ? $prevKind : 'none'));
        $nextBreakerType = (string)($nextClip['breaker_type'] ?? (!empty($nextClip['is_breaker']) ? $nextKind : 'none'));
        $prevRun = (string)($prevClip['run_state'] ?? $prevClip['motion'] ?? 'none');
        $nextRun = (string)($nextClip['run_state'] ?? $nextClip['motion'] ?? 'none');

        // Triptych transitions get strip-wise opposite-direction wipes.
        if ($prevKind === 'triptych' || $nextKind === 'triptych') {
            $invertPattern = ($prevKind === 'triptych' && $nextKind !== 'triptych');
            return [
                'name' => 'triptych_strip',
                'type' => 'triptych_strip',
                'invert' => $invertPattern,
                'duration' => 0.42,
            ];
        }

        // Explicit breaker-aware transition policy.
        if ($prevBreakerType === 'video' && $nextBreakerType === 'video') {
            return ['name' => 'dissolve', 'duration' => 0.34];
        }
        if ($nextBreakerType === 'video') {
            return ['name' => 'fadeblack', 'duration' => 0.36];
        }
        if ($prevBreakerType === 'video') {
            return ['name' => $this->runToHardTransition($nextRun), 'duration' => 0.34];
        }

        if (!empty($prevClip['is_breaker']) || !empty($nextClip['is_breaker'])) {
            $runHint = !empty($nextClip['is_breaker']) ? $prevRun : $nextRun;
            return ['name' => $this->runToHardTransition($runHint), 'duration' => 0.34];
        }

        // Within same run: reinforce motion continuity. Run change: use stronger hard transition.
        if ($prevRun !== 'none' && $prevRun === $nextRun) {
            return ['name' => $this->runToSoftTransition($prevRun), 'duration' => 0.30];
        }

        if ($nextRun !== 'none') {
            return ['name' => $this->runToHardTransition($nextRun), 'duration' => 0.32];
        }

        $variants = ['smoothleft', 'smoothright', 'smoothup', 'smoothdown'];
        return ['name' => $variants[$index % count($variants)], 'duration' => 0.30];
    }

    private function runToSoftTransition(string $runState): string {
        return match ($runState) {
            'pan_left' => 'slideleft',
            'pan_right' => 'slideright',
            'zoom_in' => 'smoothup',
            'zoom_out' => 'smoothdown',
            default => 'dissolve',
        };
    }

    private function runToHardTransition(string $runState): string {
        return match ($runState) {
            'pan_left' => 'wipeleft',
            'pan_right' => 'wiperight',
            'zoom_in' => 'wipeup',
            'zoom_out' => 'wipedown',
            default => 'fade',
        };
    }

    private function buildTriptychNormalizeCmd(
        string $inputA,
        string $inputB,
        string $inputC,
        string $outputPath,
        float $duration,
        array $dirs,
    ): array {
        $frameCount = (int)ceil($duration * self::FPS);
        $framesMinusOne = max(1, $frameCount - 1);
        $outW = $this->outputWidth();
        $outH = $this->outputHeight();
        $colWidth = (int)floor($outW / 3);
        $scaledH = (int)ceil(($outH * 1.12) / 2) * 2;
        $panT = '(n/' . $framesMinusOne . ')';
        $panEase = $this->easeInOutCubicExpr($panT);

        $yExpr1 = $this->triptychYExpr($dirs[0] ?? 'up', $panEase);
        $yExpr2 = $this->triptychYExpr($dirs[1] ?? 'down', $panEase);
        $yExpr3 = $this->triptychYExpr($dirs[2] ?? 'up', $panEase);

        $fc = implode(';', [
            "[0:v]scale=-2:{$scaledH},crop={$colWidth}:" . $outH . ":(iw-{$colWidth})/2:'{$yExpr1}'[v0]",
            "[1:v]scale=-2:{$scaledH},crop={$colWidth}:" . $outH . ":(iw-{$colWidth})/2:'{$yExpr2}'[v1]",
            "[2:v]scale=-2:{$scaledH},crop={$colWidth}:" . $outH . ":(iw-{$colWidth})/2:'{$yExpr3}'[v2]",
            "[v0][v1][v2]hstack=inputs=3[stack]",
            "[stack]drawbox=x={$colWidth}:y=0:w=2:h=" . $outH . ":color=black@0.95:t=fill,drawbox=x=" . ($colWidth * 2) . ":y=0:w=2:h=" . $outH . ":color=black@0.95:t=fill,setsar=1[vout]",
        ]);

        return [
            'ffmpeg', '-y', '-nostdin', '-hide_banner', '-loglevel', 'error',
            '-loop', '1', '-i', $inputA,
            '-loop', '1', '-i', $inputB,
            '-loop', '1', '-i', $inputC,
            '-f', 'lavfi', '-i', 'anullsrc=channel_layout=stereo:sample_rate=44100',
            '-t', number_format($duration, 2, '.', ''),
            '-filter_complex', $fc,
            '-map', '[vout]',
            '-map', '3:a',
            '-r', (string)self::FPS,
            '-c:v', 'libx264',
            '-preset', 'fast',
            '-crf', '18',
            '-c:a', 'aac',
            '-ar', '44100',
            '-ac', '2',
            '-shortest',
            '-movflags', '+faststart',
            $outputPath,
        ];
    }

    private function triptychYExpr(string $dir, string $panEaseExpr): string {
        $start = $dir === 'up' ? 0.80 : 0.20;
        $end = $dir === 'up' ? 0.20 : 0.80;
        $startStr = number_format($start, 4, '.', '');
        $endStr = number_format($end, 4, '.', '');
        $focusExpr = "{$startStr}+({$endStr}-{$startStr})*{$panEaseExpr}";
        $outH = $this->outputHeight();
        return "max(0,min(ih-" . $outH . ",(ih-" . $outH . ")*({$focusExpr})))";
    }

    private function prepareStillInputForLoop(string $inputPath, string $tempDir, int $index, string $suffix): string {
        $ext = strtolower(pathinfo($inputPath, PATHINFO_EXTENSION));
        if (!in_array($ext, ['heic', 'heif', 'avif'], true)) {
            return $inputPath;
        }

        $tempJpeg = $tempDir . '/triptych_' . $index . '_' . $suffix . '.jpg';
        $this->convertToJpeg($inputPath, $tempJpeg);
        return $tempJpeg;
    }

