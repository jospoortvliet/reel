    private function loadIncludedMedia(int $eventId, string $userId): array {
        $qb = $this->db->getQueryBuilder();
        $qb->select('m.file_id', 'm.sort_order', 'm.edit_settings', 'mem.isvideo', 'mem.video_duration', 'mem.liveid', 'mem.w', 'mem.h')
            ->from('reel_event_media', 'm')
            ->innerJoin('m', 'memories', 'mem', $qb->expr()->eq('m.file_id', 'mem.fileid'))
            ->where($qb->expr()->eq('m.event_id', $qb->createNamedParameter($eventId,  IQueryBuilder::PARAM_INT)))
            ->andWhere($qb->expr()->eq('m.user_id',   $qb->createNamedParameter($userId)))
            ->andWhere($qb->expr()->eq('m.included',  $qb->createNamedParameter(true, IQueryBuilder::PARAM_BOOL)))
            ->orderBy('m.sort_order', 'ASC');

        $result = $qb->executeQuery();
        $rows   = $result->fetchAll();
        $result->closeCursor();

        // Resolve use_live_video per row
        $outputOrientation = $this->config->getUserValue(
            $userId,
            Application::APP_ID,
            'output_orientation',
            'landscape_16_9',
        );
        $outputIsLandscape = ($outputOrientation === 'landscape_16_9');

        foreach ($rows as &$row) {
            $settings = !empty($row['edit_settings'])
                ? (json_decode($row['edit_settings'], true) ?? [])
                : [];

            if (isset($settings['use_live_video'])) {
                $row['use_live_video'] = (bool)$settings['use_live_video'];
            } elseif (!empty($row['liveid'])) {
                // Auto rule: need still dimensions.
                $dim = $this->memoriesRepository->loadStillDimensions((int)$row['file_id']);
                $stillIsLandscape = $dim && (int)$dim['w'] >= (int)$dim['h'];
                $row['use_live_video'] = ($stillIsLandscape === $outputIsLandscape);
            } else {
                $row['use_live_video'] = false;
            }
        }
        unset($row);

        return $rows;
    }

